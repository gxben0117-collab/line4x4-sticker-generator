const hotSet = new Set(HOT100);

// ── 驗證 ──
const oldCats = Object.keys(scriptData);
const unmapped = oldCats.filter(c => !MAP[c]);
const staleMap = Object.keys(MAP).filter(c => !oldCats.includes(c));
if (unmapped.length) throw new Error('未對照的舊分類：' + unmapped.join('、'));
if (staleMap.length) throw new Error('對照表有不存在的舊分類：' + staleMap.join('、'));
const allSentences = new Set(oldCats.flatMap(c => scriptData[c]));
const hotMissing = HOT100.filter(s => !allSentences.has(s));
if (hotMissing.length) throw new Error('熱門100中找不到的句子：' + hotMissing.join('、'));

// ── 去重歸類：優先序高的主分類先佔用 ──
const mainRank = Object.fromEntries(MAIN_ORDER.map((m, i) => [m, i]));
const sortedOldCats = [...oldCats].sort((a, b) => mainRank[MAP[a][0]] - mainRank[MAP[b][0]]);
const entries = new Map(); // text -> {main, sub, tags:Set}
for (const oldCat of sortedOldCats) {
  const [main, sub] = MAP[oldCat];
  for (const text of scriptData[oldCat]) {
    if (!entries.has(text)) entries.set(text, { main, sub, tags: new Set() });
    entries.get(text).tags.add(oldCat);
  }
}

// ── 組稿 ──
const stars = n => '★'.repeat(n) + '☆'.repeat(5 - n);
const tree = new Map(); // main -> sub -> [{text, star, tags}]
for (const [text, e] of entries) {
  const star = hotSet.has(text) ? 5 : (SUB_STARS[e.sub] ?? 2);
  if (!tree.has(e.main)) tree.set(e.main, new Map());
  const subs = tree.get(e.main);
  if (!subs.has(e.sub)) subs.set(e.sub, []);
  subs.get(e.sub).push({ text, star, tags: [...e.tags] });
}

